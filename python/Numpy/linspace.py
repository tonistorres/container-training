import numpy as np

# método linspace cria array com valores de ponto
# flutuante
# primeiro argumento =  valor inicial
# segundo  argumento = valor final
# terceiro argumento = quantidade de itens a gerar
# observação: não são numéros aleatórios e sim números
# igualmente espaçados

print(np.linspace(0.0, 2.0, num=5))
