"""
O math.comb()método retorna o número de maneiras de escolher k resultados não ordenados de n possibilidades, sem repetição, também conhecidos como combinações.

Nota: Os parâmetros passados ​​neste método devem ser inteiros positivos.
"""

# Import math Library
import math

# Initialize the number of items to choose from
n = 1

# Initialize the number of possibilities to choose
k = 1

# Print total number of possible combinations
print(math.comb(n, k))
