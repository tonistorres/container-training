codigo = 10
salario = 1500.00
nome = "tonis torres"
print("Código: ", codigo, "Nome: ", nome, "O salário atual é de ", salario)
# Também podemos concatenar as informações na linguagem Python
#  utilizando o sinal de soma (+).
# Neste caso, temos de converter os valores que não são string para o tipo string.
# Para isso, utilizamos o comando (str) antes da impressão da variável.
print(
    "Código: " + str(codigo) + "Nome: " + nome + "O salário atual é de " + str(salario)
)
