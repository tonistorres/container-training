"""
Definir
Os conjuntos são usados ​​para armazenar vários itens em uma única variável.

Set é um dos 4 tipos de dados internos do Python usados ​​para armazenar
coleções de dados, os outros 3 são List , Tuple e Dictionary , todos
com qualidades e usos diferentes.

Um conjunto é uma coleção não ordenada , imutável* e não indexada .

* Nota: Os itens definidos não podem ser alterados, mas você pode
remover itens e adicionar novos itens.

Os conjuntos são escritos com colchetes.

Definir itens
Os itens do conjunto não são ordenados, não podem ser alterados e não
 permitem valores duplicados.

Não ordenado
Não ordenado significa que os itens em um conjunto não têm uma ordem definida.

Os itens do conjunto podem aparecer em uma ordem diferente toda vez que você
 os usa e não podem ser referenciados por índice ou chave.

Imutável
Os itens do conjunto são imutáveis, o que significa que não podemos alterar
os itens após a criação do conjunto.

Depois que um conjunto é criado, você não pode alterar seus itens, mas pode
 remover itens e adicionar novos itens.

Duplicatas não permitidas
Os conjuntos não podem ter dois itens com o mesmo valor.


"""

# Crie um conjunto:
# Observação: os conjuntos não são ordenados, portanto,
# você não pode ter certeza em qual ordem os itens aparecerão.

thisset = {"apple", "banana", "cherry"}
print(thisset)
