"""
Definição e uso
O islower()método retorna True se todos os caracteres
estiverem em minúsculas, caso contrário, False.

Números, símbolos e espaços não são verificados, apenas caracteres alfabéticos.

"""

a = "Hello world!"
b = "hello 123"
c = "mynameisPeter"

print(a.islower())
print(b.islower())
print(c.islower())
