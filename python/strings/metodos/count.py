"""
Método Python String count()
Exemplo
Retorna o número de vezes que o valor "apple" aparece na string:
"""
txt = "I love apples, apple are my favorite fruit"

x = txt.count("apple")

print(x)

#  Pesquise da posição 10 a 24:

txt4 = "I love apples, apple are my favorite fruit"

x1 = txt4.count("apple", 10, 24)

print(x1)
