# Retorna o valor absoluto de um número. O argumento pode
# ser um inteiro, um número de ponto flutuante.

# Traduzindo: Mesmo que o resultado seja um numero negativo
# ele é convertido em positivo pela função abs


def abs_function_built_in(number):
    result = abs(number)
    print(result)


abs_function_built_in(-10.45)

abs_function_built_in(-220)
